public class ArithticOperator
{
    public static void main(String[] args)
    {
        System.out.println(10.0 / 4);
        System.out.println(10 / 4);
        double d =10 / 4;
        System.out.println(d);
        System.out.println(-10 % 3); //-1
        System.out.println(10 % -3); //1
        System.out.println(-10 % -3); //-1
        System.out.println("");
        
        int i=1;
        i = i++;
        System.out.println(i);

        i = 1;
        i = ++i;
        System.out.println(i);
    }    
}
